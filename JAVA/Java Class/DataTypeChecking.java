class DataTypeChecking
{
 public static void main(String args[])
 {
  int i = 40;
  float f = 23.98f;
  double d = 89.88;
  char c = 'A';
  boolean b = false;

  System.out.println("Different data types values are:\n" + "int : " + i + "\nFloat : " + f + "\nDouble : " + d + "\nCharacter : " + c + "\nBoolean : " + b);
 }
}