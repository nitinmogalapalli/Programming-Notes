// Creating a variable of different type

//File: VariableDeclaration.java

class VariableDeclaration
{
	public static void main(String args[])
	{
		int iVar = 3;
		double dVar = 46.79;
		float fVar = 555.09f;	
		
		System.out.println("The Int var value: " + iVar + "\nThe Double var value: " + dVar + "\nThe Float var value: " + fVar);
	}
}